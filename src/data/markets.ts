export function getMarketsWithContracts(): Market[] {
  return mockMarkets.map((m) => ({
    ...m,
    contractAddress: marketMapping[m.id] ?? m.contractAddress,
  }));
}

export function getMarketById(id: string): Market | undefined {
  return getMarketsWithContracts().find((m) => m.id === id);
}

export function getMarketsByCategory(category: string): Market[] {
  const all = getMarketsWithContracts();
  if (category === 'trending') {
    return [...all].sort((a, b) => b.volumeUsd - a.volumeUsd);
  }
  const map: Record<string, string> = {
    stock_tokens: 'stock_tokens',
    sports: 'sports',
    crypto: 'crypto',
    politics: 'politics',
    entertainment: 'entertainment',
  };
  return all.filter((m) => m.category === map[category]);
}

export function searchMarkets(query: string): Market[] {
  const q = query.toLowerCase().trim();
  if (!q) return getMarketsWithContracts();
  return getMarketsWithContracts().filter(
    (m) =>
      m.question.toLowerCase().includes(q) ||
      m.tags.some((t) => t.toLowerCase().includes(q)) ||
      m.category.includes(q),
  );
}

export function generateProbabilityHistory(market: Market): ProbabilityPoint[] {
  const points: ProbabilityPoint[] = [];
  const base = market.outcomes.map((o) => o.probability);
  for (let i = 89; i >= 0; i--) {
    const d = new Date();
    d.setHours(d.getHours() - i);
    points.push({
      time: d.toISOString(),
      probabilities: base.map((p, idx) => {
        const noise = Math.sin(i / 8 + idx) * 4 + Math.random() * 2 - 1;
        return Math.max(5, Math.min(95, p + noise));
      }),
    });
  }
  return points;
}

